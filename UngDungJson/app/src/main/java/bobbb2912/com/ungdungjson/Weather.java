package bobbb2912.com.ungdungjson;

public class Weather {
    public String ToaDo;
    public String NhietDo;
    public String ApSuat;
    public String Gio;
    public String May;
    public String Hinh;

    public String getToaDo() {
        return ToaDo;
    }

    public String getHinh() {
        return Hinh;
    }

    public void setHinh(String hinh) {
        Hinh = hinh;
    }

    public void setToaDo(String toaDo) {
        ToaDo = toaDo;
    }

    public String getNhietDo() {
        return NhietDo;
    }

    public void setNhietDo(String nhietDo) {
        NhietDo = nhietDo;
    }

    public String getApSuat() {
        return ApSuat;
    }

    public void setApSuat(String apSuat) {
        ApSuat = apSuat;
    }

    public String getGio() {
        return Gio;
    }

    public void setGio(String gio) {
        Gio = gio;
    }

    public String getMay() {
        return May;
    }

    public void setMay(String may) {
        May = may;
    }
}
